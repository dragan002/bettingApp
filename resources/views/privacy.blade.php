    <h2>1. About This App</h2>
    <p>Tipping Pool is a private, invite-only football prediction pool for a closed group of approximately 15 people. The app is not publicly open and does not accept self-registration.</p>

    <h2>2. What Data We Collect</h2>
    <p>The app collects only the minimum data needed to run the prediction pool:</p>
    <ul>
        <li><strong>Player name</strong> — set by the admin when creating your account</li>
        <li><strong>Nickname</strong> — a display name shown to the group</li>
        <li><strong>PIN</strong> — a numeric login credential stored as a secure hash</li>
        <li><strong>Predictions</strong> — your match outcome picks (1, X, 2) each gameweek</li>
        <li><strong>Token balance</strong> — tracks your participation credits (represents real money managed offline)</li>
        <li><strong>Points and streaks</strong> — your season performance data</li>
    </ul>

    <h2>3. How We Use Your Data</h2>
    <p>All data is used solely to operate the prediction pool:</p>
    <ul>
        <li>Displaying predictions, results, and leaderboards to the group</li>
        <li>Tracking token balances and transaction history</li>
        <li>Calculating points, streaks, and badges for the season</li>
    </ul>
    <p>We do not use your data for advertising, profiling, or any purpose outside the app.</p>

    <h2>4. Data Sharing</h2>
    <p>Your data is not shared with any third parties. All data remains within the private group. Token balances and predictions are visible to all members of the group as part of the app's core functionality.</p>

    <h2>5. Data Storage</h2>
    <p>Data is stored on a private server hosted on Railway.app. The server is accessible only to members of the group. No data is stored on your device beyond a session token used to keep you logged in.</p>

    <h2>6. Account Management</h2>
    <p>Accounts are created and managed exclusively by the group admin. There is no self-registration. To have your account deleted or your data removed, contact the admin directly.</p>

